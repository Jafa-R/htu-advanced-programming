package edu.htu.ap.lesson9;

public class Engine {
	int cylenders;
	int hoursePower;
	boolean turbo;
	int health;

	public int getCylenders() {
		return cylenders;
	}

	public void setCylenders(int cylenders) {
		this.cylenders = cylenders;
	}

	public int getHoursePower() {
		return hoursePower;
	}

	public void setHoursePower(int hoursePower) {
		this.hoursePower = hoursePower;
	}

	public boolean isTurbo() {
		return turbo;
	}

	public void setTurbo(boolean turbo) {
		this.turbo = turbo;
	}

	public int getHealth() {
		return health;
	}

	public void setHealth(int health) {
		this.health = health;
	}

	
}
