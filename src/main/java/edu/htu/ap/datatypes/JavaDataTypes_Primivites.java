package edu.htu.ap.datatypes;

public class JavaDataTypes_Primivites {
	
	//Primitive data types
	public static void main(String[] args) {
		byte b=10;//1-byte
		short s=100;//2-bytes
		char c='a';//2-bytes
		int i=100;//4-bytes
		float f=100.2f;//2-bytes
		long l=100033;//4-bytes
		double d=100.43;
		
		//boolean can takes only true or false values, no 0,1 or TRUE, FALSE
		boolean b1=true;
		boolean b2=false;
		//I can directly print the value 
		System.out.println(b1);
		System.out.println(c);
	}
}
